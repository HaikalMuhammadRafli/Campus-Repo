public class TeoriMandiriC14 {

    public static void main(String[] args) {

        // pengulangan akan mengulang sampai variable i mencapai 50
        for (int i = 1; i <= 50; i++) {

            // jika i modulus 3 tidak sama dengan 0, maka variable i akan di print out
            if (i % 3 != 0) {
                System.out.println(i);
            }
        }
    }
}
